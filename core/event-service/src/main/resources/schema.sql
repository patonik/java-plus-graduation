CREATE TABLE IF NOT EXISTS public.COMP_EVENT
(
    COMPILATION_ID bigint NOT NULL,
    event_id       bigint NOT NULL,
    CONSTRAINT comp_event_pkey PRIMARY KEY (COMPILATION_ID, event_id),
