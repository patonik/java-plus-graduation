    title    character varying(64) NOT NULL,
    event_id BIGINT,
    CONSTRAINT comp_pkey PRIMARY KEY (ID),
    CONSTRAINT comp_fk_event FOREIGN KEY (event_id)
        REFERENCES public.events (id)
        ON UPDATE CASCADE
        ON DELETE CASCADE
